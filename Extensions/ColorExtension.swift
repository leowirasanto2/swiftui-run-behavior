import SwiftUI

extension Color {
    public static var skeletonWhite: Color {
        return Color(hue: 1.0, saturation: 0.0, brightness: 0.968)
    }
    
    public static var lightBlack: Color {
        return Color(hue: 1.0, saturation: 0.012, brightness: 0.315)
    }
}


